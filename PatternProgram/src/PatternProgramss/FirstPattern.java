package PatternProgramss;
//                  1
//                 3*2
//                4*5*6
//               10*9*8*7

public class FirstPattern {

	public static void main(String[] args) {
		
		for(int i=1;i>10;i++)
		{
			for(int j=1;j>10;j++)
			{
				System.out.println("5");
			}
		}
		
		
	}
}
